                    Nashik has a rich historical past,as the mythology has it that Lord Rama, the King of Ayodhya, made Nashik
                    his abode during his 14 years in exile.
                  <p>  Legend states that the name "Nashik" is derived from the Hindu epic Ramayana.
                    During the exile of Lord Ram, Shoorpanakha the sister of the demon king Raavan tried to seduce Ram.
                    Angered, Raam ordered Lakshman to cut off Shoorpanakha's nose (nashika/naak).
                    According to the Raamayan, Sita was abducted by Raavan from the area called Panchavati in the Western Ghats.
                    It is a popular religious destination within the city limits, today.</p>
                   <p> The city got its present name in 1818 when the Peshwas got control of the city.
                    The Peshwa rule however, did not last long and the British captured Nashik in the very same year.
                    In 1840, one of the first modern libraries of Maharashtra (then, the Presidency of Bombay) was founded at Nashik.
                    Just 50 years after king Ashoka death Satavahana came into prominence in Western Maharashtra. its founder was Simuka.
                    Just after Simuka ended his rule, his brother Krishna took control of the throne.
                    It was during reign of Krishna, Nashik region was included in the Satavahana Empire.
                       </p>

