                    Nashik also participated in the freedom struggle of India. On 21 December 1909, 17-year-old Anant Kanhere shot the Collector of Nashik,
                    Jackson in a theatre named Vijayanand theatre, where he had gone to see a play Sharada.[9] Jackson died on the spot.
                    The people involved in the incident, Anant Laxman Kanhere, Krishnaji Gopal Karve and Vinayak Ramchandra Deshpande were sentenced to death by hanging and were hanged soon after
                    There is a confusion whether it is pronounced Nasik or Nashik.</p>
                   <p> Before 1982 both the city and district were called by the name Nasik. On 7 November 1982 the population of Nasik City exceeded to 1.2 million, so as per incorporation rules,
                    Nasik City became a Corporation.
                    Since then the city name has changed to Nashik City.</p>
